package com.octavian.operationsandcyclicfunctions;

public class AritmeticOperationsExample {
    public static void main(String[] args) {
        int a = 42;
        int c = 3;

        //Adunarea
        int sum = a + c;
        System.out.println("Suma este :" + sum);
        System.out.println("Suma este:" + (a+c));

        //Scaderea
        int sub = a-c;
        System.out.println("Rezultatul: " + sub);

        //Multification
        int result = a * c;
        System.out.println("inmultirea:" + result);

        //Devision
         result = a / c;
        System.out.println("impartirea: " + result);


    }
}
