package com.octavian.operationsandcyclicfunctions;

public class AssignmentsOperationsExample {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;

        //Atribuire simple
        int result = a;
        System.out.println("rezultatul este:" + result);

        //Atribuire cu adunare
        result += b;
        System.out.println("Rezultatul este:" + result);

        //Atribuire cu scadere
        result -= b;
        System.out.println("Rezultatul este:" + result);

        //Atribuire cu inmultire
        result *= b;
        System.out.println("Rezultatul este:" + result);

        //Atribuire cu impartire
        result /= b;
        System.out.println("Rezultatul este:" + result);


    }
}
